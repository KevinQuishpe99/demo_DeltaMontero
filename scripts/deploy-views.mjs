/* 7. V_METADATA_SISTEMA (actualizada) */
await run("V_METADATA_SISTEMA", `
CREATE OR ALTER VIEW dbo.V_METADATA_SISTEMA AS
SELECT 'VENTAS_HISTORICO' AS MODULO,
  MIN(YEAR(FECHA)) AS ANIO_MINIMO, MAX(YEAR(FECHA)) AS ANIO_MAXIMO,
  CAST(MIN(FECHA) AS DATE) AS FECHA_DESDE, CAST(MAX(FECHA) AS DATE) AS FECHA_HASTA,
  COUNT(*) AS TOTAL_REGISTROS, COUNT(DISTINCT PERIODO) AS TOTAL_PERIODOS,
  COUNT(DISTINCT LOCAL) AS TOTAL_LOCALES, COUNT(DISTINCT RUC) AS TOTAL_CLIENTES
FROM dbo.META_VENTA_NETA
UNION ALL
SELECT 'VENTAS_LIVE',
  MIN(YEAR(FECHA)), MAX(YEAR(FECHA)),
  CAST(MIN(FECHA) AS DATE), CAST(MAX(FECHA) AS DATE),
  COUNT(*), COUNT(DISTINCT PERIODO),
  COUNT(DISTINCT LOCAL), COUNT(DISTINCT RUC)
FROM dbo.V_MAESTRA_VENTAS WHERE ORIGEN='LIVE'
UNION ALL
SELECT 'CARTERA',
  MIN(YEAR(FECHA_EMISION)), MAX(YEAR(FECHA_EMISION)),
  CAST(MIN(FECHA_EMISION) AS DATE), CAST(MAX(FECHA_VENCIMIENTO) AS DATE),
  COUNT(*), COUNT(DISTINCT CONVERT(VARCHAR(6), FECHA_EMISION, 112)),
  COUNT(DISTINCT LOCAL), COUNT(DISTINCT CODIGO_CLIENTE)
FROM dbo.V_MAESTRA_CARTERA
UNION ALL
SELECT 'CIERRE_CAJA',
  MIN(YEAR(CICA_FECHA)), MAX(YEAR(CICA_FECHA)),
  CAST(MIN(CICA_FECHA) AS DATE), CAST(MAX(CICA_FECHA) AS DATE),
  COUNT(*), COUNT(DISTINCT CONVERT(VARCHAR(6), CICA_FECHA, 112)),
  COUNT(DISTINCT CICA_LOCAL), 0
FROM banda.dbo.FAC_CIERRE_CAJA
UNION ALL
SELECT 'INVENTARIO', YEAR(GETDATE()), YEAR(GETDATE()),
  CAST(GETDATE() AS DATE), CAST(GETDATE() AS DATE),
  COUNT(*), 1, COUNT(DISTINCT stk.STK_BODEGA), COUNT(DISTINCT stk.TBS_CODIGO)
FROM banda.dbo.FAC_STOCK stk JOIN banda.dbo.FAC_BIEN_SERV fbs ON stk.TBS_CODIGO=fbs.TBS_CODIGO WHERE ISNULL(fbs.TBS_ACTIVO,'S')='S'
UNION ALL
SELECT 'TESORERIA',
  MIN(YEAR(FLJ_FECHA)), MAX(YEAR(FLJ_FECHA)),
  CAST(MIN(FLJ_FECHA) AS DATE), CAST(MAX(FLJ_FECHA) AS DATE),
  COUNT(*), COUNT(DISTINCT CONVERT(VARCHAR(6), FLJ_FECHA, 112)), 0, 0
FROM banda.dbo.TES_FLUJO
UNION ALL
SELECT 'BANCOS_SALDOS', YEAR(GETDATE()), YEAR(GETDATE()),
  CAST(GETDATE() AS DATE), CAST(GETDATE() AS DATE),
  COUNT(*), 1, COUNT(*), 0
FROM banda.dbo.BCO
UNION ALL
SELECT 'PARAMETROS_IA', YEAR(GETDATE()), YEAR(GETDATE()),
  CAST(GETDATE() AS DATE), CAST(GETDATE() AS DATE),
  COUNT(*), 1, 0, 0
FROM dbo.IA_PARAMETROS_NEGOCIO;
`);

/* 8. Inventario + cierre caja (sin cambios pero asegurar que existan) */

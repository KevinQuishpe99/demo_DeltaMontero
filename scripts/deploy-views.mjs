/* 3. V_MAESTRA_VENTAS (WAREHOUSE + LIVE) */
await run("V_MAESTRA_VENTAS", `
CREATE OR ALTER VIEW dbo.V_MAESTRA_VENTAS AS
SELECT
    v.PERIODO, v.FECHA,
    YEAR(v.FECHA) AS ANIO, MONTH(v.FECHA) AS MES, DAY(v.FECHA) AS DIA,
    DATEPART(WEEK, v.FECHA) AS SEMANA, DATENAME(WEEKDAY, v.FECHA) AS DIA_SEMANA,
    DATEPART(QUARTER, v.FECHA) AS TRIMESTRE,
    v.LOCAL, v.BODEGA, v.NOMBRE_BODEGA,
    ISNULL(v.BIEN_SERVICIO,'B') AS BIEN_SERVICIO,
    v.MOVIMIENTO,
    CAST(v.SERIE AS VARCHAR(20)) AS SERIE, CAST(v.NUMERO AS VARCHAR(20)) AS NUMERO,
    v.CODIGO, v.DESCRIPCION,
    ISNULL(v.CLASIFICACION5,'') AS MARCA,
    ISNULL(v.CLASIFICACION1,'Sin Clasificar') AS LINEA_PRODUCTO,
    ISNULL(v.CLASIFICACION2,'Sin Clasificar') AS CATEGORIA,
    ISNULL(v.CLASIFICACION3,'') AS SUBCATEGORIA,
    ISNULL(v.CLASIFICACION1,'Sin Clasificar') AS CLASIFICACION1,
    ISNULL(v.CLASIFICACION2,'Sin Clasificar') AS CLASIFICACION2,
    ISNULL(v.CLASIFICACION3,'') AS CLASIFICACION3,
    ISNULL(v.RUC,'0000000000') AS RUC,
    ISNULL(v.NOMBRE_COMPLETO,'Sin Nombre') AS CLIENTE,
    ISNULL(v.CIUDAD,'Sin Ciudad') AS CIUDAD,
    ISNULL(v.PROVINCIA,'') AS PROVINCIA,
    ISNULL(v.CANTON,'') AS CANTON,
    ISNULL(v.CLAS_CLIENTE1,'Sin Segmento') AS SEGMENTO_CLIENTE,
    ISNULL(NULLIF(LTRIM(RTRIM(v.CLAS_FACTURA1)),''), ISNULL(v.CLAS_CLIENTE2,'Sin canal')) AS CANAL,
    ISNULL(v.VENDEDOR,'Sin Vendedor') AS VENDEDOR,
    ISNULL(v.CANTIDAD,0) AS CANTIDAD,
    ISNULL(v.VENTA_NETA,0) AS VENTA_NETA,
    ISNULL(v.COSTO, ISNULL(v.CANTIDAD,0)*ISNULL(v.COSTOU,0)) AS COSTO_TOTAL,
    ISNULL(v.COSTO, ISNULL(v.CANTIDAD,0)*ISNULL(v.COSTOU,0)) AS COSTO_LINEA,
    ISNULL(v.VENTA_NETA,0) - ISNULL(v.COSTO, ISNULL(v.CANTIDAD,0)*ISNULL(v.COSTOU,0)) AS UTILIDAD_BRUTA,
    CAST(CASE WHEN ISNULL(v.VENTA_NETA,0)=0 THEN 0
         ELSE (ISNULL(v.VENTA_NETA,0)-ISNULL(v.COSTO,ISNULL(v.CANTIDAD,0)*ISNULL(v.COSTOU,0)))
              /NULLIF(v.VENTA_NETA,0)*100 END AS DECIMAL(18,4)) AS MARGEN_PCT,
    ISNULL(v.PRECIO, ISNULL(v.VENTA_NETA,0)/NULLIF(v.CANTIDAD,0)) AS PRECIO_UNITARIO,
    ISNULL(v.COSTOU,0) AS COSTO_UNITARIO,
    ISNULL(v.DCTO_NETO,0)+ISNULL(v.DCTO_SUBTOTAL_NETO,0) AS DESCUENTO,
    ISNULL(v.IVA,0) AS IVA,
    CASE WHEN ISNULL(v.VENTA_NETA,0)<0 THEN 1 ELSE 0 END AS ES_DEVOLUCION,
    CASE WHEN ISNULL(v.COSTO,ISNULL(v.CANTIDAD,0)*ISNULL(v.COSTOU,0))>ISNULL(v.VENTA_NETA,0) THEN 1 ELSE 0 END AS MARGEN_NEGATIVO,
    'WAREHOUSE' AS ORIGEN
FROM dbo.META_VENTA_NETA AS v

UNION ALL

SELECT
    CONVERT(VARCHAR(6), fmo.FMO_FECHA, 112) AS PERIODO,
    fmo.FMO_FECHA AS FECHA,
    YEAR(fmo.FMO_FECHA) AS ANIO, MONTH(fmo.FMO_FECHA) AS MES, DAY(fmo.FMO_FECHA) AS DIA,
    DATEPART(WEEK, fmo.FMO_FECHA) AS SEMANA, DATENAME(WEEKDAY, fmo.FMO_FECHA) AS DIA_SEMANA,
    DATEPART(QUARTER, fmo.FMO_FECHA) AS TRIMESTRE,
    ISNULL(loc.LOC_NOMBRE, 'Local-'+CAST(fmo.FMO_LOCAL AS VARCHAR(20))) AS LOCAL,
    CAST(fmo.STK_BODEGA AS VARCHAR(20)) AS BODEGA,
    ISNULL(loc.LOC_NOMBRE, 'BOD-'+CAST(fmo.STK_BODEGA AS VARCHAR(20))) AS NOMBRE_BODEGA,
    'B' AS BIEN_SERVICIO,
    CASE fmo.FMO_TIPO WHEN 40 THEN 'FACTURA' WHEN 60 THEN 'NC' ELSE 'OTRO' END AS MOVIMIENTO,
    CAST(fmo.FMO_SERIE AS VARCHAR(20)) AS SERIE, CAST(fmo.FMO_NUMERO AS VARCHAR(20)) AS NUMERO,
    fmo.TBS_CODIGO AS CODIGO,
    ISNULL(fmo.fmo_descripcion, fbs.TBS_DESCRIPCION) AS DESCRIPCION,
    '' AS MARCA,
    ISNULL(fbs.TBS_CLAS1,'Sin Clasificar') AS LINEA_PRODUCTO,
    ISNULL(fbs.TBS_CLAS2,'Sin Clasificar') AS CATEGORIA,
    ISNULL(fbs.TBS_CLAS3,'') AS SUBCATEGORIA,
    ISNULL(fbs.TBS_CLAS1,'Sin Clasificar') AS CLASIFICACION1,
    ISNULL(fbs.TBS_CLAS2,'Sin Clasificar') AS CLASIFICACION2,
    ISNULL(fbs.TBS_CLAS3,'') AS CLASIFICACION3,
    ISNULL(cli.FCL_RUC,'0000000000') AS RUC,
    ISNULL(cli.FCL_NOMBRE_COMPLETO,'Sin Nombre') AS CLIENTE,
    ISNULL(cli.FCL_CIUDAD,'Sin Ciudad') AS CIUDAD,
    '' AS PROVINCIA,
    '' AS CANTON,
    ISNULL(cli.FCL_CLAS1,'Sin Segmento') AS SEGMENTO_CLIENTE,
    ISNULL(cli.FCL_CLAS2,'Sin canal') AS CANAL,
    ISNULL(vnd.FVE_NOMBRE,'Sin Vendedor') AS VENDEDOR,
    fmo.FMO_CANTIDAD AS CANTIDAD,
    ISNULL(fmo.FMO_PRECIO_NETO,0) AS VENTA_NETA,
    ISNULL(fmo.FMO_COSTO*fmo.FMO_CANTIDAD,0) AS COSTO_TOTAL,
    ISNULL(fmo.FMO_COSTO*fmo.FMO_CANTIDAD,0) AS COSTO_LINEA,
    ISNULL(fmo.FMO_PRECIO_NETO,0)-ISNULL(fmo.FMO_COSTO*fmo.FMO_CANTIDAD,0) AS UTILIDAD_BRUTA,
    CAST(CASE WHEN ISNULL(fmo.FMO_PRECIO_NETO,0)=0 THEN 0
         ELSE (ISNULL(fmo.FMO_PRECIO_NETO,0)-ISNULL(fmo.FMO_COSTO*fmo.FMO_CANTIDAD,0))
              /NULLIF(fmo.FMO_PRECIO_NETO,0)*100 END AS DECIMAL(18,4)) AS MARGEN_PCT,
    fmo.FMO_PRECIO AS PRECIO_UNITARIO,
    ISNULL(fmo.FMO_COSTO,0) AS COSTO_UNITARIO,
    ISNULL(fmo.FMO_DCTO_NETO,0) AS DESCUENTO,
    CAST(0 AS DECIMAL(18,2)) AS IVA,
    CASE WHEN ISNULL(fmo.FMO_PRECIO_NETO,0)<0 THEN 1 ELSE 0 END AS ES_DEVOLUCION,
    CASE WHEN ISNULL(fmo.FMO_COSTO*fmo.FMO_CANTIDAD,0)>ISNULL(fmo.FMO_PRECIO_NETO,0) THEN 1 ELSE 0 END AS MARGEN_NEGATIVO,
    'LIVE' AS ORIGEN
FROM banda.dbo.FAC_MOVIMIENTOS fmo
LEFT JOIN banda.dbo.FAC_BIEN_SERV fbs ON fmo.TBS_CODIGO=fbs.TBS_CODIGO
INNER JOIN banda.dbo.FAC_FACTURAS ffg ON fmo.FMO_SERIE=ffg.FFG_SERIE AND fmo.FMO_NUMERO=ffg.FFG_NUMERO
LEFT JOIN banda.dbo.FAC_CLIENTES cli ON ffg.FCL_CODIGO=cli.FCL_CODIGO
LEFT JOIN banda.dbo.FAC_VEND vnd ON ffg.FVE_CODIGO=vnd.FVE_CODIGO
LEFT JOIN banda.dbo.FAC_LOCALES loc ON fmo.FMO_LOCAL=loc.LOC_NUMERO
WHERE ISNULL(ffg.FFG_ANULADO,'N')='N' AND fmo.FMO_TIPO IN (40,60);
`);

/* 4. V_MAESTRA_CARTERA */
await run("V_MAESTRA_CARTERA", `
CREATE OR ALTER VIEW dbo.V_MAESTRA_CARTERA AS
SELECT
    fcc.FCC_SERIE AS SERIE,
    fcc.FCC_FACTURA AS NUMERO_FACTURA,
    fcc.FCC_TIPO AS TIPO_DOCUMENTO,
    fcc.FCC_FECHA AS FECHA_EMISION,
    fcc.FCC_VEND AS FECHA_VENCIMIENTO,
    DATEDIFF(DAY, fcc.FCC_VEND, GETDATE()) AS DIAS_VENCIDO,
    CASE
      WHEN fcc.FCC_VEND >= GETDATE() THEN 'VIGENTE'
      WHEN DATEDIFF(DAY, fcc.FCC_VEND, GETDATE()) <= 30 THEN '1-30 DIAS'
      WHEN DATEDIFF(DAY, fcc.FCC_VEND, GETDATE()) <= 60 THEN '31-60 DIAS'
      WHEN DATEDIFF(DAY, fcc.FCC_VEND, GETDATE()) <= 90 THEN '61-90 DIAS'
      ELSE 'MAS 90 DIAS'
    END AS RANGO_VENCIMIENTO,
    fcc.FCL_CODIGO AS CODIGO_CLIENTE,
    ISNULL(cli.FCL_NOMBRE_COMPLETO,'Sin Nombre') AS CLIENTE,
    ISNULL(cli.FCL_RUC,'0000000000') AS RUC,
    ISNULL(cli.FCL_CIUDAD,'Sin Ciudad') AS CIUDAD,
    ISNULL(CAST(cli.FCL_CLAS1 AS VARCHAR(50)),'Sin Segmento') AS SEGMENTO_CLIENTE,
    ISNULL(cli.FCL_CREDITO,0) AS DIAS_CREDITO,
    ISNULL(cli.FCL_LIMITE,0) AS LIMITE_CREDITO,
    fcc.FVE_CODIGO AS CODIGO_VENDEDOR,
    ISNULL(vnd.FVE_NOMBRE,'Sin Vendedor') AS VENDEDOR,
    fcc.FCC_LOCAL AS LOCAL,
    ISNULL(loc.LOC_NOMBRE,'Local-'+CAST(fcc.FCC_LOCAL AS VARCHAR)) AS NOMBRE_LOCAL,
    ISNULL(fcc.FCC_VALOR,0) AS VALOR_ORIGINAL,
    ISNULL(fcc.fcc_valor_original,0) AS VALOR_FACTURA,
    ISNULL(fcc.FCC_PAGADO,0) AS VALOR_PAGADO,
    ISNULL(fcc.FCC_VALOR,0)-ISNULL(fcc.FCC_PAGADO,0) AS SALDO_PENDIENTE,
    ISNULL(fcc.FCC_GARANTIZADO,0) AS VALOR_GARANTIZADO,
    ISNULL(fcc.fcc_ret_fuente,0) AS RETENCION_FUENTE,
    ISNULL(fcc.fcc_ret_iva,0) AS RETENCION_IVA,
    ISNULL(fcc.FCC_STATUS,'P') AS STATUS,
    CASE fcc.FCC_STATUS WHEN 'P' THEN 'PENDIENTE' WHEN 'C' THEN 'CANCELADO' WHEN 'A' THEN 'ABONADO' ELSE ISNULL(fcc.FCC_STATUS,'') END AS STATUS_DESC,
    ISNULL(fcc.FCC_RETENCION,'N') AS TIENE_RETENCION,
    CAST(GETDATE() AS DATE) AS FECHA_CONSULTA
FROM banda.dbo.FAC_CARTERA fcc
LEFT JOIN banda.dbo.FAC_CLIENTES cli ON fcc.FCL_CODIGO=cli.FCL_CODIGO
LEFT JOIN banda.dbo.FAC_VEND vnd ON fcc.FVE_CODIGO=vnd.FVE_CODIGO
LEFT JOIN banda.dbo.FAC_LOCALES loc ON fcc.FCC_LOCAL=loc.LOC_NUMERO
WHERE ISNULL(fcc.FCC_STATUS,'P') <> 'C';
`);

/* 5. V_MAESTRA_TESORERIA */
await run("V_MAESTRA_TESORERIA", `
CREATE OR ALTER VIEW dbo.V_MAESTRA_TESORERIA AS
SELECT
    tfl.FLJ_REGISTRO AS REGISTRO,
    tfl.FLJ_FECHA AS FECHA,
    YEAR(tfl.FLJ_FECHA) AS ANIO, MONTH(tfl.FLJ_FECHA) AS MES, DAY(tfl.FLJ_FECHA) AS DIA,
    DATEPART(WEEK, tfl.FLJ_FECHA) AS SEMANA,
    DATEPART(QUARTER, tfl.FLJ_FECHA) AS TRIMESTRE,
    CONVERT(VARCHAR(6), tfl.FLJ_FECHA, 112) AS PERIODO,
    tfl.DFL_CUENTA AS CUENTA,
    tfl.FLJ_DETALLE AS CONCEPTO,
    ISNULL(tfl.FLJ_VALOR,0) AS VALOR,
    ABS(ISNULL(tfl.FLJ_VALOR,0)) AS VALOR_ABS,
    CASE WHEN ISNULL(tfl.FLJ_VALOR,0)>=0 THEN 'INGRESO' ELSE 'EGRESO' END AS TIPO_MOVIMIENTO,
    CASE WHEN ISNULL(tfl.FLJ_VALOR,0)>=0 THEN ISNULL(tfl.FLJ_VALOR,0) ELSE 0 END AS INGRESO,
    CASE WHEN ISNULL(tfl.FLJ_VALOR,0)<0 THEN ABS(ISNULL(tfl.FLJ_VALOR,0)) ELSE 0 END AS EGRESO,
    CAST(GETDATE() AS DATE) AS FECHA_CONSULTA
FROM banda.dbo.TES_FLUJO tfl;
`);

